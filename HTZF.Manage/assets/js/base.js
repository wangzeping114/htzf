function load_xuanze() {
    //这段代码是全选反选
    $(".qx").click(function () {
        var checked = $(this).prop("checked") || $(this).attr("checked");
        if (checked) {
            $(".np").prop("checked", true);
            $(".np").attr("checked", true);
        }
        else {
            $(".np").removeProp("checked");
            $(".np").removeAttr("checked");
        }
    });
}